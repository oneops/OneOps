package com.oneops.boo;

public class BooCloud {
  private String name;
  private String priority;
  private String dpmt_order;
  private String pct_scale;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getPriority() {
    return priority;
  }

  public void setPriority(String priority) {
    this.priority = priority;
  }

  public String getDpmt_order() {
    return dpmt_order;
  }

  public void setDpmt_order(String dpmt_order) {
    this.dpmt_order = dpmt_order;
  }

  public String getPct_scale() {
    return pct_scale;
  }

  public void setPct_scale(String pct_scale) {
    this.pct_scale = pct_scale;
  }
}
